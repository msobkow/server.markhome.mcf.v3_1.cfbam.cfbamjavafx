package server.markhome.mcf.v3_1.cfbam.cfbamjavafx;

import server.markhome.mcf.v3_1.cflib.*;
import server.markhome.mcf.v3_1.cflib.dbutil.*;
import server.markhome.mcf.v3_1.cflib.inz.Inz;
import server.markhome.mcf.v3_1.cflib.inz.InzPathEntry;
import server.markhome.mcf.v3_1.cflib.javafx.*;

/**
 *	The CFBamJavaFX package hook is used to initialize the Inz path search among other things.
 */
public class CFBamJavaFX
{
	protected static boolean initialized = false;
		
	public static void init() {
		if (initialized) {
			return;
		}
		CFJavaFX.init();
		initialized = true;
        
		InzPathEntry entry = new InzPathEntry(CFBamJavaFX.class, "resource:/inz/langs/");
		Inz.addPathEntry(entry);
	}
}
